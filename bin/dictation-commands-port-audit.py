#!/usr/bin/env python3
"""
Phase 2 — Port-Readiness Audit for CitrusPeel libraries.

For each decompiled library:
  - find every `tell application "X"` block
  - list verbs/commands invoked against each app
  - cross-reference against current scripting dictionary (.sdef.xml)
  - flag verbs that are missing from current sdef → potential 2026-port-required

Output: analysis/sal/wwdc2016-session-717-port-audit.md
"""
import re
import os
import xml.etree.ElementTree as ET
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DECOMPILED = ROOT / "scripts/sal/dictation-commands/decompiled"
DICTS = ROOT / "dictionaries"
OUT = ROOT / "analysis/sal/wwdc2016-session-717-port-audit.md"

APP_TO_DICT = {
    "Keynote": "keynote",
    "Photos": "photos",
    "Numbers": "numbers",
    "Pages": "pages",
    "Calendar": "calendar",
    "Mail": "mail",
    "Maps": "maps",
    "Finder": "finder",
    "QuickTime Player": "quicktime-player",
    "iTunes": None,
    "Music": "music",
    "System Events": "system-events",
    "System Preferences": "system-preferences",
    "Image Events": "image-events",
}

def parse_sdef_verbs(sdef_path):
    """Return set of command/verb names defined in a .sdef.xml."""
    if not sdef_path.exists():
        return set()
    try:
        tree = ET.parse(sdef_path)
    except ET.ParseError:
        return set()
    verbs = set()
    for cmd in tree.iter("command"):
        n = cmd.get("name")
        if n:
            verbs.add(n.lower())
    return verbs

def find_tell_blocks(text):
    """Yield (app_name, body) for each `tell application "X" ... end tell`."""
    pat = re.compile(r'tell application "([^"]+)"(.*?)end tell', re.DOTALL | re.IGNORECASE)
    for m in pat.finditer(text):
        yield m.group(1), m.group(2)

def extract_verbs(body):
    """Heuristically extract first-word verbs from each non-empty line in a tell-block body."""
    verbs = set()
    for line in body.splitlines():
        s = line.strip()
        if not s or s.startswith("--"):
            continue
        m = re.match(r'(set|get|tell|if|repeat|end|on|to|return|else|try|with|using|of|its|my)\s', s, re.IGNORECASE)
        if m:
            continue
        m = re.match(r'([a-zA-Z][a-zA-Z0-9_]*)', s)
        if m:
            verbs.add(m.group(1).lower())
    return verbs

def main():
    libraries = sorted(DECOMPILED.glob("*.applescript"))
    sdef_cache = {}
    report = defaultdict(lambda: defaultdict(set))  # app -> lib -> set of verbs

    for lib_path in libraries:
        lib_name = lib_path.stem
        text = lib_path.read_text()
        for app, body in find_tell_blocks(text):
            verbs = extract_verbs(body)
            report[app][lib_name].update(verbs)

    lines = []
    lines.append("---")
    lines.append("title: WWDC 2016 Session 717 — Phase 2 Port-Readiness Audit")
    lines.append("date: 2026-05-07")
    lines.append("status: Auto-generated by bin/dictation-commands-port-audit.py from decompiled CitrusPeel libraries vs current sdef dictionaries")
    lines.append("---")
    lines.append("")
    lines.append("# Summary")
    lines.append("")
    lines.append(f"- Libraries scanned: **{len(libraries)}**")
    lines.append(f"- Distinct apps targeted: **{len(report)}**")
    lines.append("")
    lines.append("# Per-app port readiness")
    lines.append("")

    overall_unknown = 0
    overall_known = 0
    for app in sorted(report.keys()):
        dict_key = APP_TO_DICT.get(app)
        sdef_verbs = set()
        sdef_path_str = "(no current dictionary indexed)"
        if dict_key:
            sdef_path = DICTS / dict_key / f"{dict_key}.sdef.xml"
            if dict_key not in sdef_cache:
                sdef_cache[dict_key] = parse_sdef_verbs(sdef_path)
            sdef_verbs = sdef_cache[dict_key]
            if sdef_path.exists():
                sdef_path_str = f"`dictionaries/{dict_key}/{dict_key}.sdef.xml` ({len(sdef_verbs)} verbs)"

        all_verbs = set()
        for libv in report[app].values():
            all_verbs.update(libv)

        if sdef_verbs:
            unknown = sorted(v for v in all_verbs if v not in sdef_verbs and v not in {"slide", "image", "table", "chart", "row", "column", "cell", "presentation", "document", "window", "selection", "front", "first", "last", "the", "every", "all", "current", "this"})
            known = sorted(v for v in all_verbs if v in sdef_verbs)
            overall_unknown += len(unknown)
            overall_known += len(known)
            ratio = f"{len(known)}/{len(all_verbs)}"
        else:
            unknown = []
            known = []
            ratio = "n/a"

        lines.append(f"## {app}")
        lines.append("")
        lines.append(f"- Current dictionary: {sdef_path_str}")
        lines.append(f"- Used by libraries: {', '.join(sorted(report[app].keys()))}")
        lines.append(f"- Verbs invoked: **{len(all_verbs)}**, known to current sdef: **{ratio}**")
        if sdef_verbs:
            if unknown:
                lines.append(f"- ⚠ Verbs NOT in current sdef (port candidates): {', '.join(unknown[:30])}{'…' if len(unknown)>30 else ''}")
            else:
                lines.append(f"- ✅ All verbs survive in current sdef")
        lines.append("")

    lines.append("# Aggregate")
    lines.append("")
    lines.append(f"- Total verb invocations matching current sdef: **{overall_known}**")
    lines.append(f"- Total verb invocations not matching current sdef: **{overall_unknown}**")
    lines.append("")
    lines.append("**Interpretation:** the high-noise verbs (e.g. variable names that happen to start a line) are filtered out heuristically but some leak through. A verb in the 'NOT in current sdef' bucket may be: (1) a real deprecated verb requiring port, (2) a property/attribute (not a command), (3) a variable name. Manual review is required for each unknown verb in the high-priority libraries (DC-Keynote, DC-Photos, DC-Numbers).")
    lines.append("")
    lines.append("# Phase 2 next step")
    lines.append("")
    lines.append("For each unknown verb in DC-Keynote, DC-Photos, DC-Numbers, DC-Pages:")
    lines.append("1. grep the decompiled library for the verb in context")
    lines.append("2. determine if it's a real verb call or a noise match")
    lines.append("3. if real and missing: write a port stub in `scripts/sal/dictation-commands/libraries-2026/`")
    lines.append("4. if real and survives under a different name: document the rename")
    lines.append("")

    OUT.write_text("\n".join(lines))
    print(f"Wrote {OUT} ({len(lines)} lines)")
    print(f"  Apps: {len(report)}, libraries: {len(libraries)}")
    print(f"  Sdef-verified verb invocations: {overall_known}, unknown: {overall_unknown}")

if __name__ == "__main__":
    main()
