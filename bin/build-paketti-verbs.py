#!/usr/bin/env python3
"""build-paketti-verbs.py — generate Hey Sal wrappers for every Paketti
keybinding that has a chord assigned in Renoise's KeyBindings.xml.

For each Paketti <KeyBinding> with a non-empty <Key>, this writes:

  ~/work/apple/bin/renoise/<slug>          (executable bash wrapper)

…and an aggregate registry:

  ~/work/apple/analysis/sal/paketti-verbs.json

…which `hey-sal` loads at startup to dispatch utterances → wrappers.

Run:  python3 ~/work/apple/bin/build-paketti-verbs.py
"""

from __future__ import annotations
import json
import os
import re
import stat
import xml.etree.ElementTree as ET
from pathlib import Path

KB_PATH = Path.home() / "Library/Preferences/Renoise/V3.5.4/KeyBindings.xml"
OUT_DIR = Path.home() / "work/apple/bin/renoise"
REGISTRY = Path.home() / "work/apple/analysis/sal/paketti-verbs.json"
SEND = OUT_DIR / "_send"

