MODIFIER_MAP = {
    "command": "command", "cmd": "command", "meta": "command",
    "shift": "shift",
    "option": "option", "alt": "option",
    "control": "control", "ctrl": "control",
}


def parse_chord(chord: str) -> tuple[int, list[str]] | None:
    """Parse 'Shift + Command + F' → (key_code, ['shift','command']).

    Returns None if any token is unrecognised (e.g. weird unicode key)."""
    parts = [p.strip() for p in chord.split("+") if p.strip()]
    if not parts:
        return None
    mods: list[str] = []
    key_token = None
    for p in parts:
        norm = p.lower()
        if norm in MODIFIER_MAP:
            mods.append(MODIFIER_MAP[norm])
        else:
            key_token = p  # last non-modifier wins
    if key_token is None:
        return None
    code = KEY_CODES.get(key_token.upper())
    if code is None:
        code = KEY_CODES.get(key_token)
    if not isinstance(code, int):
        return None
    # Stable mod order
    mod_order = ["command", "shift", "option", "control"]
    mods = [m for m in mod_order if m in mods]
    return code, mods


_slug_strip_chars = re.compile(r"[^a-z0-9]+")


def slugify(name: str) -> str:
    """∿ Paketti Gater Dialog... → 'paketti-gater-dialog'"""
    s = name.replace("∿", "").lower()
    s = _slug_strip_chars.sub("-", s).strip("-")
    return s


def make_wrapper(path: Path, full_name: str, scope: str, chord: str,
                 key_code: int, mods: list[str]) -> None:
    mods_csv = ",".join(mods)
    chord_human = chord
    body = f"""#!/usr/bin/env bash
# Auto-generated by build-paketti-verbs.py — do not edit by hand.
# Renoise binding : {full_name}
# Renoise scope   : {scope}
# Chord           : {chord_human}
exec "$(dirname "$0")/_send" {key_code} {mods_csv}
"""
    path.write_text(body)
    path.chmod(path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)


def main() -> int:
    if not KB_PATH.exists():
        raise SystemExit(f"missing {KB_PATH}")
    if not SEND.exists():
        raise SystemExit(f"missing {SEND} — build the _send helper first")

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    REGISTRY.parent.mkdir(parents=True, exist_ok=True)

    tree = ET.parse(KB_PATH)
    root = tree.getroot()

    verbs: list[dict] = []
    skipped_unparseable: list[tuple[str, str]] = []
    slug_counts: dict[str, int] = {}

    # Top-level structure: <KeyBindings><Categories><Category><Topics><Topic>
    # …<KeyBindings><KeyBinding><Topic>Paketti</Topic>…
    # We just iterate all KeyBinding nodes.
    # Scope is the nearest enclosing <Category><Name> ancestor — climb the
    # tree using the parent map.
    parent_map = {child: parent for parent in tree.iter() for child in parent}

    def find_scope(node) -> str:
        cur = node
        while cur in parent_map:
            cur = parent_map[cur]
            if cur.tag == "Category":
                return (cur.findtext("Identifier")
                        or cur.findtext("Name") or "Unknown")
        return "Unknown"

    seen_slugs: set[str] = set()

    for kb in root.iter("KeyBinding"):
        topic = (kb.findtext("Topic") or "").strip()
        if topic != "Paketti":
            continue
        key = (kb.findtext("Key") or "").strip()
        if not key:
            continue
        binding = (kb.findtext("Binding") or "").strip()
        if not binding:
            continue

        parsed = parse_chord(key)
        if parsed is None:
            skipped_unparseable.append((binding, key))
            continue
        key_code, mods = parsed

        scope = find_scope(kb)
        slug = slugify(binding)
        # Disambiguate identical slugs (e.g. same name across scopes) by
        # appending a scope hint or numeric suffix.
        base = slug
        if slug in seen_slugs:
            scope_hint = re.sub(r"[^a-z0-9]+", "-", scope.lower()).strip("-")
            slug = f"{base}-{scope_hint}" if scope_hint else f"{base}-{slug_counts[base]+1}"
            n = 2
            while slug in seen_slugs:
                slug = f"{base}-{n}"
                n += 1
        seen_slugs.add(slug)
        slug_counts[base] = slug_counts.get(base, 0) + 1

        wrapper = OUT_DIR / slug
        make_wrapper(wrapper, binding, scope, key, key_code, mods)
        verbs.append({
            "slug": slug,
            "binding": binding,
            "scope": scope,
            "chord": key,
            "key_code": key_code,
            "modifiers": mods,
            "wrapper": str(wrapper),
        })

    REGISTRY.write_text(json.dumps({
        "generated_from": str(KB_PATH),
        "count": len(verbs),
        "skipped_unparseable_count": len(skipped_unparseable),
        "skipped_unparseable_examples": skipped_unparseable[:20],
        "verbs": verbs,
    }, indent=2))

    print(f"Wrote {len(verbs)} wrappers to {OUT_DIR}")
    print(f"Registry: {REGISTRY}")
    if skipped_unparseable:
        print(f"Skipped (unparseable chord): {len(skipped_unparseable)}")
        for b, k in skipped_unparseable[:10]:
            print(f"  - {b!r:60} chord={k!r}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
