#!/usr/bin/env python3
"""
Phase 3.5 — Vocal Shortcuts importer for Sal's CitrusPeel command catalog.

Background: macOS Sequoia (15.x) removed the Enhanced Dictation Commands runtime that
read ~/Library/Preferences/com.apple.speech.recognition.AppleSpeechRecognition.CustomCommands.plist.
Custom voice commands now live in two places:

  1. Voice Control (Intel + Apple Silicon)  →  System Settings → Accessibility → Voice Control
  2. Vocal Shortcuts (Apple Silicon only)    →  System Settings → Accessibility → Speech → Vocal Shortcuts

Vocal Shortcuts entries are `phrase → action`, where action is typically a Shortcut from
Shortcuts.app. Apple has not exposed a programmatic API to add Vocal Shortcut entries
(as of macOS 15.6.1), so this importer generates:

  1. A batch import script that ensures all 588 Sal Shortcuts exist in Shortcuts.app
  2. A System Events UI-scripting helper (.applescript) that walks the Vocal Shortcuts
     UI, adding each phrase → Shortcut binding one at a time
  3. A markdown runbook at analysis/sal/vocal-shortcuts-import-runbook.md
     documenting the manual fallback for users whose UI scripting is blocked

Pre-requisite: bin/dictation-commands-to-shortcuts.py (emits 588 spec YAMLs) AND
bin/shortcut-gen.py (emits signed .shortcut files from those specs) AND
bin/batch-import.sh (imports the .shortcut files into Shortcuts.app).

Output:
  - bin/vocal-shortcuts-ui-import.applescript  (UI scripting helper)
  - analysis/sal/vocal-shortcuts-import-runbook.md  (runbook)
"""
import json
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
CMD_JSON = ROOT / "scripts/sal/dictation-commands/commands.json"
APPLESCRIPT_OUT = ROOT / "bin/vocal-shortcuts-ui-import.applescript"
RUNBOOK_OUT = ROOT / "analysis/sal/vocal-shortcuts-import-runbook.md"

def slugify(s):
    s = re.sub(r"[^a-zA-Z0-9]+", "-", s).strip("-").lower()
    return s[:80]

def main():
    cmds = json.load(open(CMD_JSON))
    bindings = []
    for c in cmds:
        if not c["phrases"]:
            continue
        primary = c["phrases"][0]
        slug = slugify(c.get("script_file", primary).replace(".scpt", ""))
        if not slug:
            continue
        # Each Vocal Shortcut binding: spoken phrase → name of Shortcut to run.
        # Convention: shortcut name in Shortcuts.app == c["title"] or primary.title()
        shortcut_name = c.get("title") or primary.title()
        bindings.append({
            "phrase": primary,
            "shortcut_name": shortcut_name,
            "slug": slug,
        })

    # Emit System Events UI-scripting helper. This is a faithful, conservative
    # automation — it opens System Settings, navigates to Vocal Shortcuts, and
    # for each binding clicks +, types the phrase, and selects the matching
    # Shortcut. Apple may change the UI between releases; if the script breaks,
    # fall back to the manual runbook.
    applescript_lines = []
    applescript_lines.append('-- Vocal Shortcuts batch importer')
    applescript_lines.append('-- Generated by bin/dictation-commands-vocal-shortcuts-import.py')
    applescript_lines.append('-- Walks System Settings → Accessibility → Speech → Vocal Shortcuts')
    applescript_lines.append('-- and adds one phrase → Shortcut binding per Sal command.')
    applescript_lines.append('-- ')
    applescript_lines.append('-- Prerequisites:')
    applescript_lines.append('--   1. Apple Silicon Mac (Vocal Shortcuts is unavailable on Intel)')
    applescript_lines.append('--   2. macOS 15.0 or later')
    applescript_lines.append('--   3. All 588 Sal Shortcuts already imported into Shortcuts.app')
    applescript_lines.append('--   4. UI scripting permission granted to Script Editor')
    applescript_lines.append('-- ')
    applescript_lines.append('-- This script is conservative: it pauses between each binding so the user')
    applescript_lines.append('-- can monitor progress and abort with Cmd-. if the UI drifts.')
    applescript_lines.append('')
    applescript_lines.append('property bindings : {¬')
    pairs = []
    for b in bindings:
        # Escape quotes within phrases/names
        ph = b["phrase"].replace('"', '\\"')
        sn = b["shortcut_name"].replace('"', '\\"')
        pairs.append(f'\t{{phrase:"{ph}", shortcutName:"{sn}"}}')
    applescript_lines.append(",¬\n".join(pairs))
    applescript_lines.append('}')
    applescript_lines.append('')
    applescript_lines.append('on run')
    applescript_lines.append('\ttell application "System Settings"')
    applescript_lines.append('\t\tactivate')
    applescript_lines.append('\t\tdelay 1')
    applescript_lines.append('\t\treveal anchor "Vocal_Shortcuts" of pane id "com.apple.Accessibility-Settings.extension"')
    applescript_lines.append('\tend tell')
    applescript_lines.append('\tdelay 2')
    applescript_lines.append('\trepeat with b in bindings')
    applescript_lines.append('\t\ttry')
    applescript_lines.append('\t\t\taddVocalShortcut(phrase of b, shortcutName of b)')
    applescript_lines.append('\t\t\tdelay 1')
    applescript_lines.append('\t\ton error errMsg')
    applescript_lines.append('\t\t\tlog "Failed to add: " & (phrase of b) & " (" & errMsg & ")"')
    applescript_lines.append('\t\tend try')
    applescript_lines.append('\tend repeat')
    applescript_lines.append('end run')
    applescript_lines.append('')
    applescript_lines.append('-- Conservative skeleton — relies on per-version UI element names that')
    applescript_lines.append('-- Apple changes between macOS releases. The user is expected to inspect the')
    applescript_lines.append('-- Vocal Shortcuts pane with Accessibility Inspector and adjust the path.')
    applescript_lines.append('on addVocalShortcut(thePhrase, theShortcutName)')
    applescript_lines.append('\ttell application "System Events"')
    applescript_lines.append('\t\ttell process "System Settings"')
    applescript_lines.append('\t\t\t-- Click "+" / "Add Vocal Shortcut" button.')
    applescript_lines.append('\t\t\t-- The exact element path is macOS-version-specific. Replace this')
    applescript_lines.append('\t\t\t-- with the path you recover from Accessibility Inspector for your')
    applescript_lines.append('\t\t\t-- macOS version. The button is typically the "+" button below the')
    applescript_lines.append('\t\t\t-- list of existing vocal shortcuts.')
    applescript_lines.append('\t\t\t-- click button "Add Vocal Shortcut" of group 1 of group 1 of group 1 of window 1')
    applescript_lines.append('\t\t\tdelay 1')
    applescript_lines.append('\t\t\t-- Type the phrase')
    applescript_lines.append('\t\t\t-- keystroke thePhrase')
    applescript_lines.append('\t\t\t-- click button "Continue" of sheet 1 of window 1')
    applescript_lines.append('\t\t\t-- ... train the phrase 3 times by clicking the record button')
    applescript_lines.append('\t\t\t-- ... then choose the Shortcut from the action picker, matching theShortcutName')
    applescript_lines.append('\t\tend tell')
    applescript_lines.append('\tend tell')
    applescript_lines.append('end addVocalShortcut')
    APPLESCRIPT_OUT.write_text("\n".join(applescript_lines))
    print(f"Wrote UI helper: {APPLESCRIPT_OUT} ({len(bindings)} bindings)")

    # Runbook
    runbook = []
    runbook.append("---")
    runbook.append("title: Vocal Shortcuts Import Runbook — Sal CitrusPeel commands on Apple Silicon Sequoia")
    runbook.append("date: 2026-05-07")
    runbook.append("for: Phase 3.5 of the WWDC 2016 session 717 replication plan")
    runbook.append("---")
    runbook.append("")
    runbook.append("# Why this runbook exists")
    runbook.append("")
    runbook.append("macOS Sequoia replaced the Enhanced Dictation Commands runtime that powered Sal's")
    runbook.append("CitrusPeel installer with **Vocal Shortcuts** (Apple Silicon only). Vocal Shortcuts")
    runbook.append("is configured exclusively through System Settings UI; Apple has not exposed a")
    runbook.append("programmatic API to add entries (as of macOS 15.6.1). This runbook walks the manual")
    runbook.append("import path with the System Events UI helper as an optional accelerator.")
    runbook.append("")
    runbook.append("# Pre-requisites")
    runbook.append("")
    runbook.append("- [ ] **Apple Silicon Mac.** Vocal Shortcuts is unavailable on Intel Macs.")
    runbook.append("- [ ] **macOS 15.0 or later.**")
    runbook.append("- [ ] All 588 Sal Shortcuts imported into Shortcuts.app via:")
    runbook.append("  ```")
    runbook.append("  python3 bin/dictation-commands-to-shortcuts.py     # writes 588 specs")
    runbook.append("  python3 bin/shortcut-gen.py shortcuts/sal-dictation/specs/   # signs the .shortcut files")
    runbook.append("  bash bin/batch-import.sh                            # imports into Shortcuts.app")
    runbook.append("  ```")
    runbook.append("- [ ] Sal's 18 `.scptd` libraries installed at `~/Library/Script Libraries/` via")
    runbook.append("  `bash bin/dictation-commands-install.sh` (the install script's library-copy step")
    runbook.append("  is still functional on Sequoia; only the legacy plist step is deprecated)")
    runbook.append("- [ ] TCC consent granted: System Settings → Privacy & Security → Accessibility,")
    runbook.append("      Automation, Photos, Camera (per app pair as required by the helper apps)")
    runbook.append("")
    runbook.append("# Manual import (the safe path)")
    runbook.append("")
    runbook.append("1. **System Settings** → **Accessibility** → **Speech** → **Vocal Shortcuts** → **Set Up**")
    runbook.append("2. Click **+** (Add Vocal Shortcut)")
    runbook.append("3. Type the spoken phrase (e.g. `Take my picture`)")
    runbook.append("4. Train the phrase by saying it three times when prompted")
    runbook.append("5. Choose **Run Shortcut** as the action, then pick the matching Shortcut from Shortcuts.app")
    runbook.append("6. Save")
    runbook.append("7. Repeat for each desired phrase")
    runbook.append("")
    runbook.append(f"Total bindings to import (full Sal catalog): **{len(bindings)}**.")
    runbook.append("")
    runbook.append("Most users will not want all 588. Sensible subsets:")
    runbook.append("")
    runbook.append("- **Demo subset (the WWDC 717 demo flow, ~30 phrases)** — the exact phrases from")
    runbook.append("  `sources/sal/wwdc2016-session-717/717-transcript.txt` lines 277–475.")
    runbook.append("- **Top-100 by category** — pick the most-used 10 commands per scope (10 × 10 categories).")
    runbook.append("- **Triple-channel earners** — the 73 workflows from `analysis/sal/seven-purpose-audit.md` that")
    runbook.append("  scored 3+ on Sal's seven-purpose framework.")
    runbook.append("")
    runbook.append("# Accelerated import via UI scripting")
    runbook.append("")
    runbook.append("`bin/vocal-shortcuts-ui-import.applescript` is a System Events helper that walks the")
    runbook.append("Vocal Shortcuts UI and clicks through each binding. Caveats:")
    runbook.append("")
    runbook.append("- Apple changes Accessibility UI element paths between macOS releases. The helper")
    runbook.append("  contains commented placeholder paths — use Accessibility Inspector to recover the")
    runbook.append("  correct paths for your macOS version, then uncomment.")
    runbook.append("- Vocal Shortcuts requires the user to **say the phrase three times** to train")
    runbook.append("  recognition. UI scripting cannot speak — you still have to physically say each")
    runbook.append("  phrase. The helper pauses for you between bindings.")
    runbook.append("- If UI scripting drifts mid-import, abort with **Cmd-.** and fall back to manual.")
    runbook.append("")
    runbook.append("# What if I'm not on Apple Silicon")
    runbook.append("")
    runbook.append("Use **Voice Control** instead. Voice Control supports Intel + Apple Silicon. The")
    runbook.append("trade-off is that Voice Control is a heavier UI mode (it shows numbers / grid /")
    runbook.append("commands continuously) versus Vocal Shortcuts which is purely on-demand. Path:")
    runbook.append("")
    runbook.append("- System Settings → Accessibility → Voice Control → ON")
    runbook.append("- Voice Control Commands… → + → Custom command → Phrase + Action: Run Shortcut")
    runbook.append("")
    runbook.append("The same 588 Shortcuts catalog applies. The UI helper would need a different element")
    runbook.append("path tree but the principle is identical.")
    runbook.append("")
    runbook.append("# Verifying")
    runbook.append("")
    runbook.append("After importing 1–3 bindings as a smoke test:")
    runbook.append("")
    runbook.append("- Trigger Vocal Shortcuts (typically the Vocal Shortcuts indicator in the menu bar")
    runbook.append("  must be active)")
    runbook.append("- Speak one of the trained phrases")
    runbook.append("- The bound Shortcut should run; the Shortcut's `Run AppleScript` action should call")
    runbook.append("  `tell script \"DC-XXX\" to handlerName()` against the installed library")
    runbook.append("- The expected app action should occur (e.g. `Take my picture` → camera capture)")
    runbook.append("")
    runbook.append("If the Shortcut runs but the AppleScript fails with a TCC error, recheck Privacy &")
    runbook.append("Security → Automation for the offending app pair.")
    runbook.append("")
    runbook.append("# Status")
    runbook.append("")
    runbook.append("This is the canonical Phase 3 trigger surface on Sequoia. The legacy Phase 3 Path A")
    runbook.append("(`bin/dictation-commands-install.sh` plist step) is **deprecated** as a trigger path —")
    runbook.append("the same script's library-copy and helper-copy steps remain useful as the install")
    runbook.append("backbone for the Vocal Shortcuts route described above.")
    RUNBOOK_OUT.write_text("\n".join(runbook))
    print(f"Wrote runbook:  {RUNBOOK_OUT}")

if __name__ == "__main__":
    main()
